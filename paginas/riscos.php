                        function verificarRespostas() {
                            const respostasCorretas = ["c", "c"]; // Respostas corretas das duas perguntas
                            const respostasSelecionadas1 = document.querySelector('input[name="resposta1"]:checked');
                            const respostasSelecionadas2 = document.querySelector('input[name="resposta2"]:checked');

                            if (respostasSelecionadas1 && respostasSelecionadas2) {
                                const respostaUsuario1 = respostasSelecionadas1.value;
                                const respostaUsuario2 = respostasSelecionadas2.value;

                                if (respostaUsuario1 === respostasCorretas[0]) {
                                    document.getElementById("resultado1").innerHTML = "Parabéns, você acertou à pergunta 1! <br> A resposta realmente era: Coisas como vírus e bichinhos na internet.";
                                    document.getElementById("resultado1").classList.add("correta");
                                } else {
                                    document.getElementById("resultado1").innerHTML = "Poxa não foi dessa vez à pergunta 1 está incorreta! <br> A resposta seria: Coisas como vírus e bichinhos na internet.";
                                    document.getElementById("resultado1").classList.add("incorreta");
                                }

                                if (respostaUsuario2 === respostasCorretas[1]) {
                                    document.getElementById("resultado2").innerHTML = "Parabéns, você acertou à pergunta 2! <br> A resposta realmente era: Consultar e usar a internet com auxílio de um responsável";
                                    document.getElementById("resultado2").classList.add("correta");
                                } else {
                                    document.getElementById("resultado2").innerHTML = "Poxa não foi dessa vez à pergunta 2 está incorreta! <br> A resposta seria: Consultar e usar a internet com auxílio de um responsável.";
                                    document.getElementById("resultado2").classList.add("incorreta");
                                }
                            } else {
                                document.getElementById("resultado1").textContent = "Por favor, responda ambas as perguntas.";
                                document.getElementById("resultado2").textContent = "";
                                document.getElementById("resultado1").classList.add("nenhuma");
                            }
                        }

                        function recomecarQuestionario() {
                            // Limpar seleções de respostas e resultados
                            document.querySelectorAll('input[type="radio"]').forEach((input) => {
                                input.checked = false;
                            });

                            document.getElementById("resultado1").textContent = "";
                            document.getElementById("resultado2").textContent = "";

                            // Remover classes de correta/incorreta
                            document.getElementById("resultado1").classList.remove("correta", "incorreta");
                            document.getElementById("resultado2").classList.remove("correta", "incorreta");
                        }
